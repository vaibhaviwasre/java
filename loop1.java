class forloop{
	public static void main(String[]args){
		for (int i = 1;i<=5;i++){
			System.out.println("Hello student");
		}
	}
}



class whileloop{
	public static void main(String[]args){
		int i = 1;
		while(i<=5){
			System.out.println("Hello student");
			i++;
		}
	}
} 


